const mongoose = require('mongoose')
const Schema = mongoose.Schema

let userSchema = new Schema({
    firstName: {
        type: String,
        required: true
    },
    lastName: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    phone: {
        type: String,
        required: true
    }
})

let User = mongoose.model('users', userSchema)
module.exports = User
